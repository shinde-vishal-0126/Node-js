// synchronous vs asynchronous :

// 1. synchronous :
// One task at a time, sequentially.
// Blocking - Yes, tasks block the program until finished.
// Slower in I/O operations, inefficient.

// 2. asynchronous :
// Multiple tasks can be in progress concurrently.
//Blocking - No, tasks don't block the program.
// Faster, especially in I/O-bound tasks.
